
class MapFeatures {
	
	int location;
	 	MapFeatures(int location) {
	 	this.location = location;
	 	}
	 	
	class Monster extends MapFeatures{
		
		int health;
		int aggressiveness;
		
		Monster(int location, int health, int aggressiveness) {
			super(location);
			this.health = health;
			this.aggressiveness = aggressiveness;
		}
		
	}

	class Item extends MapFeatures {
		
		Item(int location) {
			super(location);
			
		}
	}
	
	class TerrainFeature extends MapFeatures {

		TerrainFeature(int location) {
			super(location);
		}
	}
	
	class Treasure extends MapFeatures {

		Treasure(int location) {
			super(location);
		}
	}
}
